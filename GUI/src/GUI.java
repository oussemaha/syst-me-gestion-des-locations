    private static void refresh_dash(){
        System.out.println("updated");
        if(dash_opened){
        long t2=System.currentTimeMillis();
        if ((t2-t)>=refresh_time){
           t=t2;
            
            dash.update(statement,penaliteJ);
            n_clients_dash.setText(String.valueOf(dash.clients));
            n_locations.setText(String.valueOf(dash.locations));
            n_appartTot.setText(String.valueOf(dash.appartTot));
            n_appartDispo.setText(String.valueOf(dash.appartDispo));
            n_revTot.setText(String.valueOf(dash.revTot));
            
        }}
    }
    public static boolean isValidNumberLength(int number) {
        String numberString = String.valueOf(number);
        int length = numberString.length();
        
        return (length >= 7 && length <= 8);
    }
